package br.com.adal.entidade;

public class OfertaDTO {
	
	private double id;
	private double oferta;
	
	public OfertaDTO() {
		
	}
	
	public double getId() {
		return id;
	}
	
	public void setId(double id) {
		this.id = id;
	}
	
	public double getOferta() {
		return oferta;
	}
	
	public void setOferta(double oferta) {
		this.oferta = oferta;
	}
}
